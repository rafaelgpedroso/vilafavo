player.bike = function(st,item)
	local _st,total

	if item and itens[item].bike==1 then
		if itens[item].rg==1 then
			if item==8 then --bicicletário
				if improve[player.hv].bike.rck<2 then
					improve[player.hv].bike.rck = improve[player.hv].bike.rck+1
				end
			else
				improve[player.hv].bike.itens = improve[player.hv].bike.itens+1
			end
		else
			_st = level[player.hv].street[st]
			_st.bike = _st.bike+1
			total = _st.bike + improve[player.hv].bike.rck + improve[player.hv].bike.itens + 2
			_st.tbike = math.random(total*5,total*10)
		end
	end
	if not item or itens[item].bike==1 and itens[item].rg==1 then
		for st,_st in pairs(level[player.hv].street) do
			total = _st.bike + improve[player.hv].bike.rck + improve[player.hv].bike.itens + 2
			_st.tbike = math.random(total*5,total*10)
		end
	end
end

player.control = function()
	local pollution, accident, jam, access, st, _st, happ, mx, mn, need, rgi, rge
	local bn = levelc[player.lv].bn
	local fl = 1 - bn
	
	player.pollution, player.accident, player.jam, player.access = 0,0,0,0
	player.stjam = false
	
	if player.lv>=11 then
		rgi = 1
		rge = 3
	elseif player.lv>=6 then
		rgi = 1
		rge = 2
	else
		rgi = 1
		rge = 1
	end
	
	rgtotal, rgnr = 0,0
	sttotal, stnr = 0,0
	bdtotal, bdnr = 0,0
	lntotal, lnnr = nr.ln,0
	
	for n=rgi,rge do
		rgtotal = rgtotal + nr.rg
		for item,_item in pairs(ad.rg) do
			if improve[n].itens[item] then
				rgnr = rgnr+1
			end
		end

		for st,_st in pairs(level[n].street) do
			sttotal = sttotal + nr.st

			for item,_item in pairs(ad.st) do
				if improve[n].street[st] and improve[n].street[st][item] then
					stnr = stnr+1
				end
			end
		end
		
		for bd,_bd in pairs(level[n].build) do
			bdtotal = bdtotal + nr.bd

			for item,_item in pairs(ad.bd) do
				if improve[n].build[bd] and improve[n].build[bd][item] then
					bdnr = bdnr+1
				end
			end
		end
		
		for ln,_ln in pairs(improve[n].lnbus) do
			lnnr = lnnr + _ln
		end
		
		if player.lv>=itens[8].lv then --bicicletário
			rgtotal = rgtotal + 1
			rgnr = rgnr + improve[n].bike.rck/2
		end
		
		if improve[n].itens[13] then --rodizio
			rgtotal = rgtotal + 2
			rgnr = rgnr + 2
		end
	end

	if player.lv>=itens[5].lv then --centro transito
		rgtotal = rgtotal + 1
		if improve[1].itens[5] then
			rgnr = rgnr + 1
		end
	end
	
	if player.lv>=itens[17].lv then --plano zoneamento
		rgtotal = rgtotal + 15
		if improve[2].itens[17]	then
			rgnr = rgnr + 15
		end
	end
	
	happ = (rgnr+stnr+bdnr+lnnr) / (rgtotal+sttotal+bdtotal+lntotal) 
	
	happ = 	(bn + happ * fl) * 100
	
	mn = true
	need = levelc[player.lv].need
	if need then
		if need.t=='rg' then
			mn = improve[need.hv].itens[need.i] 
		elseif need.t=='stbus' then
			mn = improve[need.hv].stbus[need.i] and improve[need.hv].stbus[need.i][need.n] 
		end
	end

	if not mn or player.stjam then
		mx = 0.7
	else
		mx = 1
	end
	
	player.happ = happ * mx - player.decr
	
	print('HAPP',player.happ)
	
	if player.happ>=75 then
		menu.active = false
		onKey('ENTER',nil)
		onKey('CURSOR_DOWN',nil)
		onKey('CURSOR_UP',nil)
		onKey('CURSOR_LEFT',nil)
		onKey('CURSOR_RIGHT',nil)
		painel.on = true

		player.lv = player.lv + 1
		player.newLevel()
	elseif player.happ<=0 then
		place.stop()
		timer.stop()

		painel:remove()
		buttons:remove()
		cursor.hidden = true

		box.i = {'GAME OVER!!','Oh não! Infelizmente os investimentos que você fez não foram o bastante para melhorar a mobilidade em Vila Favo e não foi possível atingir o grau de satisfação da população. Que pena, tente novamente em outra oportunidade!'}
		box.draw()
		
		onKey('CURSOR_DOWN',nil)
		onKey('CURSOR_UP',nil)
		onKey('CURSOR_LEFT',nil)
		onKey('CURSOR_RIGHT',nil)

		onKey('ENTER',function()
			box.draw(true)
			buttons:remove()
			control:remove()
			imsg:remove()
			player.close()
			splash.on()
		end)
	end
end

