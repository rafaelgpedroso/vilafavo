player.improve = function(implant,i,item)
	if implant=='lnbus' then
		improve[player.hv].stbus[i][item] = player.hv..item..'0'
		player.update('street',i,1)
		player.cash('h',itens[1].pr*-1)
	elseif implant=='region' then
		improve[i].itens[item] = true
		if item==5 then
			improve[1].itens[item] = true
			improve[2].itens[item] = true
			improve[3].itens[item] = true
		elseif item==17 then
			improve[1].itens[item] = true
			improve[2].itens[item] = true
			improve[3].itens[item] = true
		elseif  item==15 and player.rail==1 then
			improve[1].itens[item] = true
			improve[2].itens[item] = true
		end
		
		player.update(implant,nil,item)
		player.cash('h',itens[item].pr*-1)
		player.region()
	elseif implant=='street' or implant=='build' then
		
		if item==8 or item==12 or item==16 then
			improve[player.hv].itens[item] = true
			player.update('region',nil,item)
		else
			improve[player.hv][implant][i][item] = true
			player.update(implant,i,item)

			if item==11 then
				improve[player.hv][implant][i][10] = true
			elseif item==7 then
				improve[player.hv][implant][i][6] = true
			end
		end
		player.cash('h',itens[item].pr*-1)
	end
	player.bike(i,item)
end

player.update = function(implant,i,item)
	local c,t,rnd,incr, st,_st

	if itens[item].rg==1 then
		implant = 'region'
	end

	if implant=='region' then
		if item==8 then
			if improve[player.hv].bike.rck<2 then
				incr = 0.5
			end		
		elseif item==17 then
			incr=3
		elseif  item==15 and player.rail==1 then
			incr=2
		else
			incr=1
		end
		incr = incr/#level[player.hv].street
		for st,_st in pairs(level[player.hv].street) do
			for mk=1,3 do
				if item==13 then
					_st.total[mk] = _st.total[mk]+incr
				end
				if _st.total[mk]>_st.implanted[mk] then
					if itens[item].sc[mk]==1 then
						_st.implanted[mk] = _st.implanted[mk]+incr
						
						if _st.jam then
							_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*70 + math.random(0,10)
						else
							_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*70 + math.random(10,20)
						end
					end
				end
			end
			if _st.jam and _st.implanted[3]/_st.total[3]>0.65 then
				street.s[st].jam = false
				_st.jam = false
			end
		end
		
		if item==15 then
			level[1].street[1].jam = false
			level[2].street[1].jam = false
			if level[3].street[1] then
				level[3].street[1].jam = false
			end
		end
	elseif implant=='street' then
		_st = level[player.hv].street[i]
		
		for mk=1,3 do
			if itens[item].sc[mk]==1 then
				if _st.total[mk]>_st.implanted[mk] then
					incr = 1
					if item==11 and not improve[player.hv].street[i][10] then
						incr = 2
					elseif item==7 and not improve[player.hv].street[i][6] then
						incr = 2
					end
					
					_st.implanted[mk] = _st.implanted[mk] + incr
					if _st.jam then
						_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*70 + math.random(0,10)
					else
						_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*80 + math.random(10,20)
					end
				end
			end
		end

		if _st.jam and _st.implanted[3]/_st.total[3]>=0.65 then
			street.s[i].jam = false
			_st.jam = false
		end
		
	elseif implant=='build' then
		local _build = level[player.hv].build[i]

		if _build.implanted<_build.total then
			_build.implanted = _build.implanted + 1
			_build.marker = 5 + _build.implanted/_build.total*70 + math.random(10,20)
		end
	end

	place.start()
	player.control()
end

