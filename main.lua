	if t=='c' then
		player.coin = player.coin + v
		player.cplus.t = 30
		player.cplus.v = v>0 and '+'..v or ''..v
	else
		player.honey = player.honey + v
		player.hplus.t = 30
		player.hplus.v = v>0 and '+'..v or ''..v
	end
end

