ad = {}
nr = {}

player.target = function()
	local st,_st, lnbus,_lnbus, strch,_strch, build,_build, item,_item
	local stbus = {}
	
	target={itens={},st={},stbus={},build={}}

	ad = {rg={},st={},bd={},ln={}}
	nr = {rg=0,st=0,bd=2,ln=0}
	
	ad.bd[2] = true
	ad.bd[3] = true
	
	for item,_item in pairs(itens) do
		if _item.lv<=player.lv then
			if _item.ad==1 then
				ad.st[item] = _item.tl
				nr.st = nr.st+1
			elseif _item.ad==2 then
				ad.rg[item] = _item.tl
				nr.rg = nr.rg+1
			end
		end
	end

	for n=1,3 do
		if n==1 or n==2 and player.lv>=6 or n==3 and player.lv>=11 then
			for lnbus,_lnbus in pairs(level[n].lnbus) do
				nr.ln = nr.ln+#_lnbus-1			
			end

			target.itens = {total={0,0,0},implanted={0,0,0}}
			for item,_item in pairs(itens) do
				if _item.rg==1 and _item.lv<=player.lv then
					for mk=1,3 do
						if _item.sc[mk]==1 then
							if item ~=13 then
								target.itens.total[mk] = target.itens.total[mk] + 1
							end
							if item==8 then
								target.itens.implanted[mk] = target.itens.implanted[mk] + improve[n].bike.rck/2
							elseif improve[n].itens[item] then
								if item ==13 then
									target.itens.total[mk] = target.itens.total[mk] + 1
								end
								target.itens.implanted[mk] = target.itens.implanted[mk] + 1
							end
						end
					end
				end
			end
			level[n].itens = {total=target.itens.total,implanted=target.itens.implanted}
		
			for build,_build in pairs(level[n].build) do
				_build.total = 2
				_build.implanted = 0

				if improve[n].build[build] and improve[n].build[build][2] then
					_build.implanted = _build.implanted + 0.5
				end
				if improve[n].build[build] and improve[n].build[build][3] then
					_build.implanted = _build.implanted + 1.5
				end
				
				if not _build.marker then
					_build.marker = 5 + _build.implanted/_build.total*70 + math.random(0,20)
				end
			end
		
			if #level[n].street > 0 then
				for mk=1,3 do
					target.itens.total[mk] = target.itens.total[mk] / #level[n].street
					target.itens.implanted[mk] = target.itens.implanted[mk] / #level[n].street
				end
			end
			
			for st,_st in pairs(level[n].street) do
				target.st[st] = {total={0,0,0},implanted={0,0,0}}
				_st.bike = 0
				for item,_item in pairs(itens) do
					if item>1 and _item.lv<=player.lv and _item.rg==0 and _item.excl<2 then
						for mk=1,3 do
							if _item.sc[mk]==1 then
								if _item.sc[mk]==1 then
									target.st[st].total[mk] = target.st[st].total[mk] + 1

									if improve[n].street[st] and improve[n].street[st][item] then
										target.st[st].implanted[mk] = target.st[st].implanted[mk] + 1
										if _item.bike==1 then
											_st.bike = _st.bike+1
										end
									end
								end
							end
						end
					end
				end

				stbus[st] = {}
				target.stbus[st] = {total={0,0,0},implanted={0,0,0}}

				for lnbus,_lnbus in pairs(level[n].lnbus) do
					for strch,_strch in pairs(_lnbus) do
						if _strch.st and _strch.st==st then
							stbus[st][lnbus] = true			
						end
					end
				end
				
				for lnbus,_lnbus in pairs(stbus[st]) do
					target.stbus[st].total[1] = target.stbus[st].total[1] + 1
					target.stbus[st].total[3] = target.stbus[st].total[3] + 1
					if improve[n].stbus[st] and improve[n].stbus[st][lnbus] then
						target.stbus[st].implanted[1] = target.stbus[st].implanted[1] + 1
						target.stbus[st].implanted[3] = target.stbus[st].implanted[3] + 1
					end
				end
				
				_st.total = {}
				_st.implanted = {}
				
				_st.tbike = 0
				
				for mk=1,3 do
					_st.total[mk] = target.st[st].total[mk] + target.stbus[st].total[mk] + target.itens.total[mk]
					_st.implanted[mk] = target.st[st].implanted[mk] + target.stbus[st].implanted[mk] + target.itens.implanted[mk]

					if _st.marker[mk] == 0 then
						if _st.jam then
							_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*70 + math.random(0,10)
						else
							_st.marker[mk] = 5 + _st.implanted[mk]/_st.total[mk]*70 + math.random(10,20)
						end
					end
				end
				if _st.implanted[3]/_st.total[3]>=0.65 then
					_st.jam = false
				end
			end
		end
	end
	player.bike()
	player.control()
end

